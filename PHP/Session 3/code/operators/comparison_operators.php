<?php

$breakNewline = "<br>\n";

/**
 * Equal
 */
var_dump( 4 == 5);

echo $breakNewline;

/**
 * Identical
 */
var_dump(4 === "4");

echo $breakNewline;

/**
 * not equal
 */

var_dump(4 != 4);

echo $breakNewline;

/**
 * less than
 */

var_dump(4 >= 4);

echo $breakNewline;

/**
 * Spaceship
 */
var_dump(3 <=> 4);